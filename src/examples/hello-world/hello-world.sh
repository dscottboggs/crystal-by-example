# To run the program, put the code in `hello-world.cr` and use the `crystal` command.
$ crystal hello-world.cr
Hello, World!

# Once you're done changing your program, you'll want to compile it to a binary. That's what the `crystal build` command is for.
$ crystal build hello-world.cr
$ ls
hello-world     hello-world.cr

# We can then execute the binary directly
$ ./hello-world
Hello, World!
